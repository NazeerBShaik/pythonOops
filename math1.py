import math

#to get min and max vlues in an iterable
x = min(5, 39, 94)
y = max(5, 39, 94)

print(x,y)

# to get the absolute positive value of the specified number
x = abs(-3.43)
print(x)

# to get the power of a value
x = pow(4, 3)
print(x)

# math.sqrt() method 
x = math.sqrt(64)
print(x)

# math.ciel(), math.floor() methods 
x = math.ceil(1.4)
y = math.floor(1.4) 
print(x, y)

# math.pi 
x = math.pi
print(x)

